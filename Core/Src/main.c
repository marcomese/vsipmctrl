/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

    fsm_t parseFSM;
    fsm_t packetCtrlFSM;
    fsm_t sendFSM;

    arm_pid_instance_f32 biasPID;
    biasPID.Kp = PIDKp;
    biasPID.Ki = PIDKi;
    biasPID.Kd = PIDkd;

    arm_pid_instance_f32 katPID;
    katPID.Kp = PIDKp;
    katPID.Ki = PIDKi;
    katPID.Kd = PIDkd;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

    memset(dataBuffer1,0,BUFSIZE);
    memset(dataBuffer2,0,BUFSIZE);
    memset(sendBuffer,0,BUFSIZE);
    memset(packetToProcess,0,BUFSIZE);
    memset(packetToSend,0,BUFSIZE);

    ADCBuf[0] = 0;
    ADCBuf[1] = 0;
    ADCBuf[2] = 0;

    currPacket = packetToProcess[0];

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_DAC_Init();
  MX_USART1_UART_Init();
  MX_USART2_UART_Init();
  MX_TIM2_Init();
  /* USER CODE BEGIN 2 */

    arm_pid_init_f32(&biasPID, 1);
    arm_pid_init_f32(&katPID, 1);

    HAL_UART_Receive_IT(&huart1, dataBuffer1, 1);
    HAL_UART_Receive_IT(&huart2, dataBuffer2, 1);

    packetCtrlFSM = initFSM(packetCtrlIDLE,
                            packetCtrlInputs,
                            packetCtrlOutputs);

    parseFSM = initFSM(parseIDLE,
                       parseFsmInputs,
                       parseFsmOutputs);

    sendFSM = initFSM(fsmSendIDLE,
                      sendFsmInputs,
                      sendFsmOutputs);

    HAL_DAC_Start(&hdac, DAC1_CHANNEL_1);
    HAL_DAC_Start(&hdac, DAC1_CHANNEL_2);

    HAL_ADC_Start_DMA(&hadc1, ADCBuf, 3);

    HAL_TIM_Base_Start_IT(&htim2);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
      runFSM(&packetCtrlFSM);

      currPacket = packetToProcess[packetReadIndex];

      runFSM(&parseFSM);

      execute(command);

      runFSM(&sendFSM);

      execute(sendCommand);

      if(timeElapsed){
          pidErrBias = biasVoltage-biasReadVal;
          pidOutBias = arm_pid_f32(&biasPID, pidErrBias);
          biasDACVal = pidOutBias*4095/2.9;

          HAL_DAC_SetValue(&hdac, DAC1_CHANNEL_1, DAC_ALIGN_12B_R, biasDACVal);

          pidErrKat = katodeVoltage-katodeReadVal;
          pidOutKat = arm_pid_f32(&katPID, pidErrKat);
          katDACVal = pidOutKat*4095/2.9;

          HAL_DAC_SetValue(&hdac, DAC1_CHANNEL_2, DAC_ALIGN_12B_R, katDACVal);

          timeElapsed = 0;
      }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

